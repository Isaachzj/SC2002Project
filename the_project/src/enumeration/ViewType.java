package enumeration;

public enum ViewType {
	/**
	 * For the enumeration of the ViewType, there are 3 types (STANDARD, OCEAN, CITY)
	 * This indicates room view as to what the guest is able to view from their respective hotel rooms
	 * 
	 * STANDARD is used to indicate the standard view
	 * OCEAN is used to indicate the view of the ocean - the shallow turquoise ocean that invites the fresh smell of sea salt upon the opening of the window
	 * CITY is used to indicate the view of the city - bustling with full of life as there are indistinguishable chatter among the street vendors
	 */
	STANDARD, OCEAN, CITY
}
